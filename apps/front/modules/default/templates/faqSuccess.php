<h3>About</h3>
<dl>
  <dt>Why another site?</dt>
  <dd>We needed some specific features for the young people in our locality that were not present on ReadHisWord.com so we decided to write our own.</dd>
</dl>
<dl>
  <dt>Different versions of the Bible?</dt>
  <dd>We use the Recovery Version.</dd>
</dl>
<dl>
  <dt>Where can I get a copy of the New Testament bible?</dt>
  <dd>Get it from freestudybibles.com.</dd>
</dl>

<h3>Features</h3>
<dl>
  <dt>How is BibleGen diffrent from RHW?</dt>
  <dd><ul>
    <li>Generate a schedule based on desired amount of time (no need to guess your last schedule date!).</li>
    <li>Support for scheduling by chapters. No need to break up a chapter by verses!</li>
    <li>Provide smart handling of long and/or short chapters making daily readings reasonable and uniform in length. ** Note: This is not supported for verses.</li>
    <li>Birds-eye view for serving ones to check on the progress of young peoples' bible reading - hey, occasional encouragement is needed, right?</li>
    <li>Mobile edition (possibly Android app).</li>
  </ul></dd>
</dl>
<dl>
  <dt>Will Old Testament reading schedules be available on BibleGen?</dt>
  <dd>Yes, definitely. At this point, I'm focusing on fine-tuning the features of this site and adding more smart handling with the generation of the New Testament schedules. Once everything looks good to go, I will add in support for the Old Testament.</dd>
</dl>
<dl>
  <dt>Will you support footnotes in the readings?</dt>
  <dd>No and possibly never. Please use ReadHisWord.com if you want to include footnotes in your reading.</dd>
</dl>

<h3>Algorithms</h3>
<dl>
  <dt>Are all these complicated calculations necessary? Why not just a regular schedule?</dt>
  <dd>Yes, when you deal with young people who will negotiate and whine about too little time and "the chapter was sooo long!".</dd>
</dl>